/** This example was designed to run in a web browser's JavaScript console,
 * but it can also be run with Node.js as long as it doesn't include DOM examples. */

(function () {
  'use strict';
  // Operators
  console.log(10 + 11);
  console.log(10 - 11);
  console.log(10 * 20);
  console.log(13 / 40);
  console.log(40 % 2);

  // Hierarchy of operators
  console.log(3 + 5 / 2(3 + 5) / 2);

  // Infinity
  console.log(1 / 0);
  // -Infinity
  console.log(-1 / 0);

  // NaN
  console.log(0 / 0);
})();
