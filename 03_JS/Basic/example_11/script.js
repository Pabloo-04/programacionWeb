// If statement

(function () {
  'use strict';

  if (2 >= 3) {
    console.log(0);
  } else {
    console.log(1);
  }

  if (2 >= 3) {
    console.log(0);
  } else if (4 < 2) {
    console.log(1);
  } else {
    console.log(2);
  }
})();
